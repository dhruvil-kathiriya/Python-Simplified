### this is \\ Double backslash

print("this is \\\\ Double backslash")

### these are /\/\/\/\/\ mountains

print("this are /\\/\\/\\/\\ mountains")

### he is    awesome (Use escape sequence instead of manual spaces)

print("he is \t awesome")

### \" \n \t \' (print this as an output)

print("\\\" \\n \\t \\' ")